let currentLang = localStorage.getItem("scory-lang") || "fr";

/** @returns {"fr"|"en"} */
export function getLang() { return currentLang; }

/** @param {"fr"|"en"} lang */
export function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("scory-lang", lang);
  document.documentElement.lang = lang;
  // Mettre a jour tous les elements avec data-i18n
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const t = TRANSLATIONS[key];
    if (!t) return;
    const attr = el.getAttribute("data-i18n-attr");
    if (attr === "placeholder") {
      el.placeholder = t[lang] || t.fr;
    } else {
      el.textContent = t[lang] || t.fr;
    }
  });
}

/** @param {string} key @returns {string} */
export function t(key) {
  const entry = TRANSLATIONS[key];
  return entry ? (entry[currentLang] || entry.fr) : key;
}
